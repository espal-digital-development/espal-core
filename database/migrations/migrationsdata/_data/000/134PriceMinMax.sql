CREATE TABLE "PriceMinMax" (
	"id" UUID PRIMARY KEY DEFAULT gen_random_uuid(),
	"createdByID" UUID NOT NULL,
	"updatedByID" UUID,
	"bundledProductID" UUID,
	"productModelID" UUID,
	"productVariantID" UUID,
	"domainID" UUID NOT NULL,
	"priceGroupID" UUID NOT NULL,
	"taxGroupID" UUID NOT NULL,
	"currency" INT2 NOT NULL,
	"createdAt" TIMESTAMP NOT NULL DEFAULT NOW(),
	"updatedAt" TIMESTAMP,
	"lowest" DECIMAL(9,2) NOT NULL,
	"highest" DECIMAL(9,2) NOT NULL,
	INDEX "PriceMinMax_idx_createdByID" ("createdByID"),
	INDEX "PriceMinMax_idx_updatedByID" ("updatedByID"),
	INDEX "PriceMinMax_idx_bundledProductID" ("bundledProductID"),
	INDEX "PriceMinMax_idx_productModelID" ("productModelID"),
	INDEX "PriceMinMax_idx_productVariantID" ("productVariantID"),
	INDEX "PriceMinMax_idx_domainID" ("domainID"),
	INDEX "PriceMinMax_idx_priceGroupID" ("priceGroupID"),
	INDEX "PriceMinMax_idx_taxGroupID" ("taxGroupID"),
	INDEX "PriceMinMax_idx_currency" ("currency"),
	INDEX "PriceMinMax_idx_lowest" ("lowest"),
	INDEX "PriceMinMax_idx_highest" ("highest"),
	CONSTRAINT "PriceMinMax_createdByID" FOREIGN KEY ("createdByID") REFERENCES "User" ("id"),
	CONSTRAINT "PriceMinMax_updatedByID" FOREIGN KEY ("updatedByID") REFERENCES "User" ("id"),
	CONSTRAINT "PriceMinMax_bundledProductID" FOREIGN KEY ("bundledProductID") REFERENCES "BundledProduct" ("id"),
	CONSTRAINT "PriceMinMax_productModelID" FOREIGN KEY ("productModelID") REFERENCES "ProductModel" ("id"),
	CONSTRAINT "PriceMinMax_productVariantID" FOREIGN KEY ("productVariantID") REFERENCES "ProductVariant" ("id"),
	CONSTRAINT "PriceMinMax_domainID" FOREIGN KEY ("domainID") REFERENCES "Domain" ("id"),
	CONSTRAINT "PriceMinMax_priceGroupID" FOREIGN KEY ("priceGroupID") REFERENCES "PriceGroup" ("id"),
	CONSTRAINT "PriceMinMax_taxGroupID" FOREIGN KEY ("taxGroupID") REFERENCES "TaxGroup" ("id"),
	FAMILY "Primary" ("id", "createdByID", "bundledProductID", "productModelID", "productVariantID",
		"domainID", "priceGroupID", "taxGroupID", "createdAt"),
	FAMILY "Secondary" ("updatedByID", "updatedAt", "currency", "lowest", "highest")
);
