CREATE TABLE "WishListLine" (
	"id" UUID PRIMARY KEY DEFAULT gen_random_uuid(),
	"createdByID" UUID NOT NULL,
	"updatedByID" UUID,
	"wishListID" UUID NOT NULL,
	"productVariantID" UUID,
	"bundledProductID" UUID,
	"createdAt" TIMESTAMP NOT NULL DEFAULT NOW(),
	"updatedAt" TIMESTAMP,
	"sorting" INT NOT NULL DEFAULT 0,
	"quantity" INT NOT NULL,
	INDEX "WishListLine_idx_createdByID" ("createdByID"),
	INDEX "WishListLine_idx_updatedByID" ("updatedByID"),
	INDEX "WishListLine_idx_wishListID" ("wishListID"),
	INDEX "WishListLine_idx_productVariantID" ("productVariantID"),
	INDEX "WishListLine_idx_bundledProductID" ("bundledProductID"),
	CONSTRAINT "WishListLine_createdByID" FOREIGN KEY ("createdByID") REFERENCES "User" ("id"),
	CONSTRAINT "WishListLine_updatedByID" FOREIGN KEY ("updatedByID") REFERENCES "User" ("id"),
	CONSTRAINT "WishListLine_wishListID" FOREIGN KEY ("wishListID") REFERENCES "WishList" ("id"),
	CONSTRAINT "WishListLine_productVariantID" FOREIGN KEY ("productVariantID") REFERENCES "ProductVariant" ("id"),
	CONSTRAINT "WishListLine_bundledProductID" FOREIGN KEY ("bundledProductID") REFERENCES "BundledProduct" ("id"),
    FAMILY "Primary" ("id", "createdByID", "createdAt", "wishListID", "productVariantID", "bundledProductID"),
	FAMILY "Secondary" ("updatedByID", "updatedAt", "sorting", "quantity")
);
