CREATE TABLE "UserAddress" (
	"id" UUID PRIMARY KEY DEFAULT gen_random_uuid(),
	"createdByID" UUID NOT NULL,
	"updatedByID" UUID,
	"userID" UUID NOT NULL,
	"createdAt" TIMESTAMP NOT NULL DEFAULT NOW(),
	"updatedAt" TIMESTAMP,
	"active" BOOL NOT NULL DEFAULT false,
    "firstName" STRING(50),
	"surname" STRING(50),
	"street" STRING(72) NOT NULL,
	"streetLine2" STRING(72),
	"number" STRING(12) NOT NULL,
	"numberAddition" STRING(12),
	"zipCode" STRING(12) NOT NULL,
	"city" STRING(255) NOT NULL,
	"state" STRING,
	"country" INT2,
	"phoneNumber" STRING(32),
	"email" STRING(255),
	INDEX "UserAddress_idx_createdByID" ("createdByID"),
	INDEX "UserAddress_idx_updatedByID" ("updatedByID"),
	INDEX "UserAddress_idx_userID" ("userID"),
	CONSTRAINT "UserAddress_createdByID" FOREIGN KEY ("createdByID") REFERENCES "User" ("id"),
	CONSTRAINT "UserAddress_updatedByID" FOREIGN KEY ("updatedByID") REFERENCES "User" ("id"),
	CONSTRAINT "UserAddress_userID" FOREIGN KEY ("userID") REFERENCES "User" ("id"),
    FAMILY "Primary" ("id", "createdByID", "updatedByID", "userID", "createdAt", "updatedAt",
        "active", "firstName", "surname", "street", "streetLine2", "number", "numberAddition",
        "zipCode", "state", "country", "phoneNumber", "email")
);
