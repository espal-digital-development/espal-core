CREATE TABLE "ProductModelReview" (
	"id" UUID PRIMARY KEY DEFAULT gen_random_uuid(),
	"createdByID" UUID NOT NULL,
	"updatedByID" UUID,
	"productModelID" UUID NOT NULL,
	"reviewedByID" UUID NOT NULL,
	"createdAt" TIMESTAMP NOT NULL DEFAULT NOW(),
	"updatedAt" TIMESTAMP,
    "reviewedOnDate" TIMESTAMP,
    "reviewNotes" STRING(255),
    "approved" BOOL,
    "rating" DECIMAL(9,6),
    "title" STRING(255),
    "description" STRING,
	INDEX "ProductModelReview_idx_createdByID" ("createdByID"),
	INDEX "ProductModelReview_idx_updatedByID" ("updatedByID"),
	INDEX "ProductModelReview_idx_productModelID" ("productModelID"),
	INDEX "ProductModelReview_idx_reviewedByID" ("reviewedByID"),
    CONSTRAINT "ProductModelReview_createdByID" FOREIGN KEY ("createdByID") REFERENCES "User" ("id"),
    CONSTRAINT "ProductModelReview_updatedByID" FOREIGN KEY ("updatedByID") REFERENCES "User" ("id"),
    CONSTRAINT "ProductModelReview_productModelID" FOREIGN KEY ("productModelID") REFERENCES "ProductModel" ("id"),
    CONSTRAINT "ProductModelReview_reviewedByID" FOREIGN KEY ("reviewedByID") REFERENCES "User" ("id"),
    FAMILY "Primary" ("id", "createdByID", "createdAt", "productModelID", "reviewedByID", "reviewedOnDate"),
	FAMILY "Secondary" ("updatedByID", "updatedAt", "reviewNotes", "approved", "rating", "title", "description")
);
